
public class NoResultsException extends Exception {
	  /**
	 * 
	 */
	private static final long serialVersionUID = -1961360865561186413L;
	public NoResultsException() { super(); }
	  public NoResultsException(String message) { super(message); }
	  public NoResultsException(String message, Throwable cause) { super(message, cause); }
	  public NoResultsException(Throwable cause) { super(cause); }
}